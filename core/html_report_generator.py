from typing import List
from report_components.base_component import ReportComponent
import html
from datetime import datetime


class HTMLReportGenerator:
    """Generates a standalone HTML report with PDF download capability."""

    def __init__(self, report_title: str = "Data Quality Analysis Report"):
        self.report_title = report_title

    def generate(self, components: List[ReportComponent], output_path: str = "report.html") -> str:
        """Generate HTML report and save to file."""
        html_content = self._build_html(components)

        with open(output_path, 'w', encoding='utf-8') as f:
            f.write(html_content)

        return output_path

    def _build_html(self, components: List[ReportComponent]) -> str:
        sections_html = ""
        toc_html = ""

        for i, component in enumerate(components):
            component_name = component.__class__.__name__
            section_id = f"section-{i}"

            # Build table of contents
            toc_html += f'<li><a href="#{section_id}">{self._format_name(component_name)}</a></li>\n'

            # Build section content
            sections_html += self._build_section(component, section_id, i + 1)

        return f"""<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>{html.escape(self.report_title)}</title>
    <style>
        {self._get_styles()}
    </style>
</head>
<body>
    <div class="container">
        <header>
            <h1>{html.escape(self.report_title)}</h1>
            <p class="timestamp">Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}</p>
            <button onclick="window.print()" class="download-btn">
                üì• Download as PDF
            </button>
        </header>

        <nav class="toc no-print">
            <h2>Table of Contents</h2>
            <ul>
                {toc_html}
            </ul>
        </nav>

        <main>
            {sections_html}
        </main>

        <footer>
            <p>Generated by AEDA - Automated Exploratory Data Analysis</p>
        </footer>
    </div>

    <script>
        {self._get_scripts()}
    </script>
</body>
</html>"""

    def _build_section(self, component: ReportComponent, section_id: str, number: int) -> str:
        name = self._format_name(component.__class__.__name__)

        try:
            raw_justification = component.justify() or "N/A"
            justification = self._format_justification(raw_justification)
        except Exception:
            justification = "Component analysis incomplete"

        try:
            summary = component.summarize() or {}
        except Exception:
            summary = {"Status": "Analysis failed or skipped"}

        summary_rows = ""
        for key, value in summary.items():
            formatted_value = self._format_value(value)
            summary_rows += f"""
            <tr>
                <td class="key">{html.escape(str(key))}</td>
                <td class="value">{formatted_value}</td>
            </tr>"""

        llm_explanations_html = ""
        component_summary_html = ""
        is_dataset_summary = component.__class__.__name__ == "LLMDatasetSummaryComponent"

        if hasattr(component, 'get_full_summary'):
            full_summary = component.get_full_summary()
            if full_summary and full_summary.strip():
                llm_section, summary_section = self._parse_full_summary(full_summary, is_dataset_summary)

                if llm_section:
                    llm_explanations_html = f"""
                <div class="llm-explanations">
                    <h4>üîç Semantic Explanations</h4>
                    <div class="llm-content">{llm_section}</div>
                </div>"""

                if summary_section:
                    if is_dataset_summary:
                        component_summary_html = f"""
                <div class="component-summary-box dataset-summary-box">
                    <h4>üìä Analysis Overview</h4>
                    <div class="summary-content-html">{summary_section}</div>
                </div>"""
                    else:
                        component_summary_html = f"""
                <div class="component-summary-box">
                    <h4>üìã Component Summary</h4>
                    <div class="summary-content">{summary_section}</div>
                </div>"""

        return f"""
        <section id="{section_id}" class="component-section">
            <h2><span class="section-number">{number}</span> {html.escape(name)}</h2>

            <div class="justification">
                <h3>Component Description</h3>
                <div class="justification-content">{justification}</div>
            </div>

            <div class="summary">
                <h3>Summary</h3>
                <table>
                    <tbody>
                        {summary_rows}
                    </tbody>
                </table>
            </div>

            {llm_explanations_html}
            {component_summary_html}
        </section>"""

    def _parse_full_summary(self, full_summary: str, is_dataset_summary: bool = False) -> tuple:
        """Parse full summary into semantic explanations and component summary sections."""
        llm_section = ""
        summary_section = ""

        lines = full_summary.split('\n')
        current_section = None
        current_content = []
        ai_assessment_content = []

        for line in lines:
            if 'ü§ñ LLM EXAMPLE EXPLANATIONS' in line or 'LLM EXPLANATIONS' in line:
                current_section = 'llm'
                current_content = []
            elif 'üìã COMPONENT SUMMARY' in line or 'üìä COMPONENT METRICS OVERVIEW' in line:
                if current_section == 'llm':
                    llm_section = '\n'.join(current_content)
                current_section = 'summary'
                current_content = []
            elif 'ü§ñ AI QUALITY ASSESSMENT' in line:
                if current_section == 'summary':
                    summary_section = '\n'.join(current_content)
                current_section = 'ai_assessment'
                current_content = []
            elif '=' * 10 in line:
                continue
            elif current_section:
                current_content.append(line)

        if current_section == 'llm':
            llm_section = '\n'.join(current_content)
        elif current_section == 'summary':
            summary_section = '\n'.join(current_content)
        elif current_section == 'ai_assessment':
            ai_assessment_content = current_content

        if not llm_section and not summary_section and full_summary.strip():
            summary_section = full_summary

        llm_section = html.escape(llm_section.strip())

        if is_dataset_summary and summary_section:
            summary_section = self._format_metrics_as_html(summary_section.strip())
            if ai_assessment_content:
                summary_section += self._format_ai_assessment_as_html(ai_assessment_content)
        else:
            summary_section = html.escape(summary_section.strip())

        return llm_section, summary_section

    def _format_metrics_as_html(self, metrics_text: str) -> str:
        """Format component metrics as a nice HTML grid."""
        lines = metrics_text.split('\n')
        cards = []
        current_card = None

        for line in lines:
            line = line.strip()
            if not line:
                continue
            if line.startswith('üìå'):
                if current_card:
                    cards.append(current_card)
                current_card = {'title': line.replace('üìå', '').strip(), 'metrics': []}
            elif line.startswith('‚Ä¢') and current_card:
                current_card['metrics'].append(line.replace('‚Ä¢', '').strip())

        if current_card:
            cards.append(current_card)

        if not cards:
            return html.escape(metrics_text)

        html_parts = ['<div class="metrics-grid">']
        for card in cards:
            metrics_html = ''.join([f'<div class="metric-item">{html.escape(m)}</div>' for m in card['metrics']])
            html_parts.append(f'''
                <div class="metric-card">
                    <div class="metric-title">{html.escape(card['title'])}</div>
                    {metrics_html}
                </div>
            ''')
        html_parts.append('</div>')

        return ''.join(html_parts)

    def _format_ai_assessment_as_html(self, ai_lines: list) -> str:
        """Format AI assessment as a beautiful HTML table with strengths and weaknesses."""
        text = '\n'.join(ai_lines)

        strengths = []
        issues = []
        priority = ""

        current_section = None
        for line in ai_lines:
            line = line.strip()
            if not line:
                continue
            if '‚úÖ' in line and 'STRENGTH' in line.upper():
                current_section = 'strengths'
                continue
            elif '‚ùå' in line and 'ISSUE' in line.upper():
                current_section = 'issues'
                continue
            elif 'üéØ' in line and 'PRIORITY' in line.upper():
                current_section = 'priority'
                continue

            if current_section == 'strengths' and line.startswith(('‚Ä¢', '-', '*')):
                strengths.append(line.lstrip('‚Ä¢-* '))
            elif current_section == 'issues' and line.startswith(('‚Ä¢', '-', '*')):
                issues.append(line.lstrip('‚Ä¢-* '))
            elif current_section == 'priority':
                priority = line.lstrip('‚Ä¢-* ')

        if not strengths and not issues:
            return f'<div class="ai-assessment-text">{html.escape(text)}</div>'

        strengths_html = ''.join([f'<tr><td class="strength-icon">‚úÖ</td><td>{html.escape(s)}</td></tr>' for s in strengths])
        issues_html = ''.join([f'<tr><td class="issue-icon">‚ùå</td><td>{html.escape(i)}</td></tr>' for i in issues])

        priority_html = ""
        if priority:
            priority_html = f'''
                <div class="priority-action">
                    <span class="priority-icon">üéØ</span>
                    <span class="priority-text"><strong>Priority Action:</strong> {html.escape(priority)}</span>
                </div>
            '''

        return f'''
            <div class="ai-assessment-container">
                <h5>AI Quality Assessment</h5>
                <div class="assessment-tables">
                    <div class="assessment-column strengths-column">
                        <h6>‚úÖ Strengths</h6>
                        <table class="assessment-table">
                            {strengths_html}
                        </table>
                    </div>
                    <div class="assessment-column issues-column">
                        <h6>‚ùå Issues</h6>
                        <table class="assessment-table">
                            {issues_html}
                        </table>
                    </div>
                </div>
                {priority_html}
            </div>
        '''

    def _format_name(self, name: str) -> str:
        """Convert CamelCase to readable format."""
        import re
        if name == "LLMDatasetSummaryComponent":
            return "Dataset Summary"
        name = name.replace("Component", "").replace("Report", "")
        return re.sub(r'(?<!^)(?=[A-Z])', ' ', name)

    def _format_justification(self, text: str) -> str:
        """Convert markdown-style formatting in justifications to HTML."""
        import re

        # Escape HTML first
        text = html.escape(text)

        # Convert **bold** to <strong>
        text = re.sub(r'\*\*(.+?)\*\*', r'<strong>\1</strong>', text)

        # Convert *italic* to <em>
        text = re.sub(r'\*(.+?)\*', r'<em>\1</em>', text)

        # Convert `code` to <code>
        text = re.sub(r'`(.+?)`', r'<code>\1</code>', text)

        # Split into paragraphs by double newlines
        paragraphs = re.split(r'\n\s*\n', text)

        formatted_parts = []

        for para in paragraphs:
            para = para.strip()
            if not para:
                continue

            # Check if this paragraph contains numbered list items
            lines = para.split('\n')
            list_items = []
            non_list_lines = []

            for line in lines:
                line = line.strip()
                if not line:
                    continue
                # Check for numbered list (1. 2. 3.)
                match = re.match(r'^(\d+)\.\s+(.+)$', line)
                if match:
                    list_items.append(match.group(2))
                else:
                    # Check for bullet list (- or *)
                    match = re.match(r'^[-*]\s+(.+)$', line)
                    if match:
                        list_items.append(match.group(1))
                    else:
                        non_list_lines.append(line)

            # Output non-list content as paragraph
            if non_list_lines:
                formatted_parts.append(f'<p>{" ".join(non_list_lines)}</p>')

            # Output list items
            if list_items:
                items_html = ''.join([f'<li>{item}</li>' for item in list_items])
                formatted_parts.append(f'<ol>{items_html}</ol>')

        return ''.join(formatted_parts) if formatted_parts else text

    def _format_value(self, value) -> str:
        """Format values for HTML display."""
        if isinstance(value, float):
            if abs(value) < 1:
                return f"{value:.4f}"
            return f"{value:.2f}"
        elif isinstance(value, list):
            if not value:
                return "<em>None</em>"
            # Check if it's a list of dictionaries (example outliers, etc.)
            if isinstance(value[0], dict):
                return self._format_examples_list(value)
            # Check if it's a list of tuples (worst_columns, etc.)
            elif isinstance(value[0], tuple) and len(value[0]) == 2:
                return self._format_tuple_list(value)
            elif len(value) > 5:
                return html.escape(str(value[:5])) + f"... ({len(value)} items)"
            return html.escape(str(value))
        elif isinstance(value, dict):
            items = [f"<strong>{html.escape(str(k))}:</strong> {html.escape(str(v))}"
                     for k, v in list(value.items())[:10]]
            return "<br>".join(items)
        return html.escape(str(value))

    def _format_examples_list(self, examples: list) -> str:
        """Format a list of example dictionaries as beautiful HTML cards."""
        if not examples:
            return "<em>No examples</em>"

        # Limit to first 5 examples
        examples = examples[:5]

        html_parts = [f'<div class="examples-container"><span class="examples-count">{len(examples)} example(s)</span>']

        for i, example in enumerate(examples, 1):
            # Determine the type of example and format accordingly
            card_content = self._format_single_example(example, i)
            html_parts.append(card_content)

        html_parts.append('</div>')
        return ''.join(html_parts)

    def _format_single_example(self, example: dict, index: int) -> str:
        """Format a single example dictionary as an HTML card."""
        # Identify key fields for different types of examples
        row_idx = example.get('row_index', example.get('index', None))

        # Build header
        if row_idx is not None:
            header = f"Row {row_idx}"
        elif 'column' in example:
            header = f"Column: {example['column']}"
        else:
            header = f"Example {index}"

        # Build metrics based on what's in the example
        metrics = []

        # Common fields to display nicely
        field_labels = {
            'outlier_score': ('Score', lambda v: f"{v:.3f}"),
            'score': ('Score', lambda v: f"{v:.3f}"),
            'frequency': ('Frequency', lambda v: f"{v:.4f}"),
            'error': ('Error', lambda v: f"{v:.4f}"),
            'reconstruction_error': ('Recon. Error', lambda v: f"{v:.4f}"),
            'similarity': ('Similarity', lambda v: f"{v:.1%}"),
            'confidence': ('Confidence', lambda v: f"{v:.1%}"),
            'noise_score': ('Noise Score', lambda v: f"{v:.3f}"),
            'value': ('Value', lambda v: html.escape(str(v))),
            'column': ('Column', lambda v: html.escape(str(v))),
            'narrative': ('Details', lambda v: html.escape(str(v)[:100])),
            'explanation_narrative': ('Details', lambda v: html.escape(str(v)[:100])),
        }

        for field, (label, formatter) in field_labels.items():
            if field in example and example[field] is not None:
                try:
                    formatted_val = formatter(example[field])
                    metrics.append(f'<div class="example-metric"><span class="metric-label">{label}:</span> <span class="metric-value">{formatted_val}</span></div>')
                except:
                    pass

        # Handle top_contributing_features specially
        if 'top_contributing_features' in example:
            features = example['top_contributing_features']
            if isinstance(features, dict):
                top_3 = list(features.items())[:3]
                features_str = ', '.join([f"{k}: {v:.2f}" for k, v in top_3])
                metrics.append(f'<div class="example-metric"><span class="metric-label">Top Features:</span> <span class="metric-value">{html.escape(features_str)}</span></div>')

        # Handle matching/differing columns for near-duplicates
        if 'matching_columns' in example:
            cols = example['matching_columns'][:3]
            metrics.append(f'<div class="example-metric"><span class="metric-label">Matching:</span> <span class="metric-value">{html.escape(", ".join(cols))}</span></div>')

        if 'differing_columns' in example:
            cols = example['differing_columns'][:3]
            metrics.append(f'<div class="example-metric"><span class="metric-label">Differing:</span> <span class="metric-value">{html.escape(", ".join(cols))}</span></div>')

        metrics_html = ''.join(metrics) if metrics else '<div class="example-metric"><em>No details</em></div>'

        return f'''
            <div class="example-card">
                <div class="example-header">{html.escape(header)}</div>
                <div class="example-body">{metrics_html}</div>
            </div>
        '''

    def _format_tuple_list(self, tuples: list) -> str:
        """Format a list of tuples (like worst_columns) as a nice list."""
        if not tuples:
            return "<em>None</em>"

        items = []
        for name, value in tuples[:5]:
            if isinstance(value, float):
                items.append(f'<div class="tuple-item"><strong>{html.escape(str(name))}:</strong> {value:.2%}</div>')
            else:
                items.append(f'<div class="tuple-item"><strong>{html.escape(str(name))}:</strong> {html.escape(str(value))}</div>')

        if len(tuples) > 5:
            items.append(f'<div class="tuple-item"><em>... and {len(tuples) - 5} more</em></div>')

        return '<div class="tuple-list">' + ''.join(items) + '</div>'

